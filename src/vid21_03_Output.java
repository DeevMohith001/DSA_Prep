import java.util.Arrays;

public class vid21_03_Output {
    public static void main(String[] args) {
        Integer num = new Integer(56);
        System.out.println(num.toString()); // output 56
        System.out.println(num);

        System.out.println(Arrays.toString(new int[]{2, 3, 4, 5})); // output [2, 3, 4, 5]

        String name = null;
        System.out.println(name); // output null
    }
}
